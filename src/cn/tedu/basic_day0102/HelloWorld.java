package cn.tedu.basic_day0102;

/**
 * package - 包
 * cn.tedu - 包名，全小写，域名反转规则（公司的域名倒着写 www.baidu.com - com.baidu）
 * 注意：在Java代码中出现的符号均为英文符号
 */

/**
 * public - 公共的，作为类的修饰符，修饰谁都可以来访问这个类
 * class - 类，Java用来定义类的一个特殊的单词
 * HelloWorld - 类名：驼峰命名法，每个单词的首字母都要大写
 * {} - 花括号表示类的主体内容，我们写的代码就放在这里面
 */
public class HelloWorld {
    /**
     * 入口函数，程序的入口
     * public static void main(String[] args) {}
     */
    public static void main(String[] args) {
        System.out.println("你好，北部湾！！");

    }
}
